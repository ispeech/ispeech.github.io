				<p>一、你們相信原先設定的願景嗎？
				當您接觸到新創公司創辦人和團隊時，最先要問的就是請他們闡述公司的願景，以及使他們每天早晨起床的動力是什麼。您可以評估：他們是否真正相信公司的願景，而這些願景能不能夠打動您。
				以 Airbnb 為例，他們的願景是「讓人們感覺像是回到家一般，不論是在世界何處。」如果你是一個相信深入當地環境才能真正體會外國文化的人，那麼你的願景就與他們一致。
